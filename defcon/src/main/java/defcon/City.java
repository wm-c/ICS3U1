package defcon;

public class City{
    
    public int cityX;
    public int cityY;
    public boolean exists = true;
    public int population;
    public String city_Name;
    public int width;
    public int height;
    
    public City(int cityX, int cityY, boolean exists, int population, String city_Name, int width, int height){
        this.cityX = cityX;
        this.cityY = cityY;
        this.exists = exists;
        this.population = population;
        this.city_Name = city_Name;
        this.width = width;
        this.height = height;
    }





}